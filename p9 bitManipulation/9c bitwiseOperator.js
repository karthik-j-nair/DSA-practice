// and operator
console.log(15 & 5);   // op => 5 : 1111 & 101 = 101 (both operand 1 then only result 1 else 0)

// or operator
console.log(15 | 5);   // op => 15 : 1111 & 101 = 1111  (1 | 1 or 1 | 0 will always be one)

// xor operator
console.log(15 ^ 5);   // op => 10 : (opoosite will be 1 and similar will be 0)

//  right shift operator
console.log(15 >> 2);  // op => 3 : right shift 2 bits to the right

// complement operator
let n = 10;
console.log(~n);    // op => -11 : formule -(n + 1)
